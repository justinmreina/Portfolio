/* @fcn		  getSideNavWidth()
 * @brief	  get side nav width for use
 * @details	  x                       				
 *
 * @ret  (String) field width (e.g. "123px")
 */ 																													                                                               
/************************************************************************************************************************************/
function getSideNavWidth() {

	//Locals
	var isMobile;

	//Check
	isMobile = getDeviceType();

	//Return value
	if(isMobile) {
		return SIDENAV_WIDTH_MOBILE;
	} else {
		return SIDENAV_WIDTH_DESK;
	}
}
